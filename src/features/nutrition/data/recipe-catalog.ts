const ALL_RESTRICTIONS: DietaryRestriction[] = [
  'vegetarian',
  'vegan',
  'gluten_free',
  'lactose_free',
  'halal',
  'kosher',
];

/** Helper: returns the restriction list with the listed conflicts removed. */
function compatibleWith(
  ...exclude: DietaryRestriction[]
): DietaryRestriction[] {
  return ALL_RESTRICTIONS.filter((r) => !exclude.includes(r));
}

const stepKey = (recipeId: string, count: number): string[] =>
  Array.from(
    { length: count },
    (_, i) => `nutrition.recipes.items.${recipeId}.steps.${i + 1}`,
  );

